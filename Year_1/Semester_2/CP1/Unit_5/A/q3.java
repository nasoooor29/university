package Unit_5.A;

public class q3 {
    public static void simpleForLoop() {
        final int NUMBER_OF_ITERATIONS = 10; // assign the number of times you want the loop to contenue with a constant
        for (int i = 1; i <= NUMBER_OF_ITERATIONS; i++) { // create for loop and it will break when the increment reach
                                                          // the NUMBER_OF_ITERATIONS
            System.out.println(i); // printing the incrementer
        }

    }
}
